    private boolean isPrime(int angka) {
        if (angka <= 1) return false; // Bilangan kurang dari atau sama dengan 1 bukan bilangan prima
        for (int i = 2; i <= Math.sqrt(angka); i++) {
            if (angka % i == 0) return false; // Jika ada pembagi lain selain 1 dan angka itu sendiri
        }
        return true; 
    }
        
