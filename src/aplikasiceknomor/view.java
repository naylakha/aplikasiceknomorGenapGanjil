    private void btnCekPrimaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCekPrimaActionPerformed
         try {
            int angka = Integer.parseInt(txtInput.getText());

            // Cek apakah angka adalah bilangan prima dan tampilkan hasilnya dengan JOptionPane
            if (isPrime(angka)) {
                JOptionPane.showMessageDialog(this, "Angka " + angka + " adalah Bilangan Prima.");
            } else {
                JOptionPane.showMessageDialog(this, "Angka " + angka + " bukan Bilangan Prima.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Input tidak valid! Masukkan angka.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCekPrimaActionPerformed

