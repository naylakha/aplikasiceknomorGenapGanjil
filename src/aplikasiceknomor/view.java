    private void btnCekActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCekActionPerformed
        try {
        int angka = Integer.parseInt(txtInput.getText());

        // Cek apakah angka ganjil atau genap
        if (angka % 2 == 0) {
            lblResult.setText("Angka " + angka + " adalah Genap.");
        } else {
            lblResult.setText("Angka " + angka + " adalah Ganjil.");
        }
    } catch (NumberFormatException e) {
        lblResult.setText("Input tidak valid! Masukkan angka.");
    }


        
    }//GEN-LAST:event_btnCekActionPerformed

